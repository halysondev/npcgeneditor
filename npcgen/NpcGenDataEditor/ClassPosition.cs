namespace NpcGenDataEditor;

public class ClassPosition
{
	public float PosX;

	public float PosY;

	public float PosZ;

	public float DirX;

	public float DirY;

	public float DirZ;

	public ClassPosition(float X, float Y, float Z, float DX, float DY, float DZ)
	{
		PosX = X;
		PosY = Y;
		PosZ = Z;
		DirX = DX;
		DirY = DY;
		DirZ = DZ;
	}
}
